public class Execute {

    private int a;
    private int b;
    private int c;
    private int result;

    public Execute() {
    }

    public Execute(int a, int b, int c, int result) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.result = result;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public int getResult() {
        return result;
    }

    public void setResult(int result) {
        this.result = result;
    }
    
    
}
