public class Execute {
    String str;
    String opt;
    int res;

    public Execute() {
    }

    public Execute(String str, String opt, int res) {
        this.str = str;
        this.opt = opt;
        this.res = res;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public String getOpt() {
        return opt;
    }

    public void setOpt(String opt) {
        this.opt = opt;
    }

    public int getRes() {
        return res;
    }

    public void setRes(int res) {
        this.res = res;
    }
    
    
}
