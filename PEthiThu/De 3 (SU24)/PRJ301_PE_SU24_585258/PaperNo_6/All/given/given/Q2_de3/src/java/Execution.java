public class Execution {
    String arr;
    String opt;
    int res;

    public Execution() {
    }

    public Execution(String arr, String opt, int res) {
        this.arr = arr;
        this.opt = opt;
        this.res = res;
    }

    public String getArr() {
        return arr;
    }

    public void setArr(String arr) {
        this.arr = arr;
    }

    public String getOpt() {
        return opt;
    }

    public void setOpt(String opt) {
        this.opt = opt;
    }

    public int getRes() {
        return res;
    }

    public void setRes(int res) {
        this.res = res;
    }
    
    
}
